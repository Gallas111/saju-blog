export interface BranchRelations {
  chung: Branch; // 충
  samhap: Branch[]; // 삼합 파트너 2개
  samhapElement: Element;
  yukhap: Branch; // 육합
}

export function getBranchRelations(branchIdx: number): BranchRelations {
  const group = SAMHAP_GROUPS.find((s) => s.members.includes(branchIdx))!;
  const yukPair = YUKHAP_PAIRS.find((p) => p.includes(branchIdx))!;
  return {
    chung: BRANCHES[(branchIdx + 6) % 12],
    samhap: group.members
      .filter((m) => m !== branchIdx)
      .map((m) => BRANCHES[m]),
    samhapElement: group.element,
    yukhap: BRANCHES[yukPair[0] === branchIdx ? yukPair[1] : yukPair[0]],
  };
}

const ELEMENT_ORDER: Element[] = ["목", "화", "토", "금", "수"];

function elementIndex(e: Element): number {
  return ELEMENT_ORDER.indexOf(e);
}

// 일간 기준 일지 본기의 십성 계산
export function getSipseong(stemIdx: number, branchIdx: number): string {
  const stem = STEMS[stemIdx];
  const target = STEMS[BRANCHES[branchIdx].mainStem];
  const diff =
    (elementIndex(target.element) - elementIndex(stem.element) + 5) % 5;
  const samePolarity = stem.yinYang === target.yinYang;
  switch (diff) {
    case 0: return samePolarity ? "비견" : "겁재";
    case 1: return samePolarity ? "식신" : "상관";
    case 2: return samePolarity ? "편재" : "정재";
    case 3: return samePolarity ? "편관" : "정관";
    default: return samePolarity ? "편인" : "정인";
  }
}

export interface Gapja {
  index: number;
  name: string; // 갑자
  hanja: string; // 甲子
  slug: string; // gapja
  stem: Stem;
  branch: Branch;
  sipseong: string;
  image: string;
  story: string;
  postSlug: string;
}

function romanize(stem: Stem, branch: Branch): string {
  // 국어의 로마자 표기법 연음 예외 (기존 블로그 슬러그와 통일)
  if (stem.ko === "갑" && branch.ko === "오") return "gabo";
  if (stem.ko === "갑" && branch.ko === "인") return "gabin";
  return stem.roman + branch.roman;
}

export const GAPJA_LIST: Gapja[] = Array.from({ length: 60 }, (_, i) => {
  const stem = STEMS[i % 10];
  const branch = BRANCHES[i % 12];
  return {
    index: i,
    name: stem.ko + branch.ko,
    hanja: stem.hanja + branch.hanja,
    slug: romanize(stem, branch),
    stem,
    branch,
    sipseong: getSipseong(i % 10, i % 12),
    image: COMBOS[i].image,
    story: COMBOS[i].story,
    postSlug: POST_SLUGS[i],
  };
});

export function getGapjaBySlug(slug: string): Gapja | undefined {
  return GAPJA_LIST.find((g) => g.slug === slug);
}

// 그레고리력 → 율리우스적일 (정오 기준 JDN)
export function getJdn(year: number, month: number, day: number): number {
  const a = Math.floor((14 - month) / 12);
  const y = year + 4800 - a;
  const m = month + 12 * a - 3;
  return (
    day +
    Math.floor((153 * m + 2) / 5) +
    365 * y +
    Math.floor(y / 4) -
    Math.floor(y / 100) +
    Math.floor(y / 400) -
    32045
  );
}

// 일주 인덱스 (0 = 갑자). 검증: 파일 상단 주석 참고.
export function getIljuIndex(year: number, month: number, day: number): number {
  return (((getJdn(year, month, day) + 49) % 60) + 60) % 60;
}

export function getIlju(year: number, month: number, day: number): Gapja {
  return GAPJA_LIST[getIljuIndex(year, month, day)];
}
